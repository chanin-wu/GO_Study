package main

import (
	"net/http"

	"github.com/gin-gonic/gin"
)

/*
Gin 中的中间件是在路由处理函数之前（以及可选地之后）运行的函数。它们用于日志记录、认证、错误恢复和请求修改等横切关注点。

Gin 支持三个级别的中间件附加

	全局中间件 — 应用于路由器中的每个路由。使用 router.Use() 注册。适用于日志记录和 panic 恢复等普遍适用的关注点
	分组中间件 — 应用于路由组中的所有路由。使用 group.Use() 注册。适用于将认证或授权应用到路由子集（例如所有 /admin/* 路由）
	路由级中间件 — 仅应用于单个路由。作为额外参数传递给 router.GET()、router.POST() 等。适用于路由特定的逻辑，如自定义限流或输入验证

执行顺序： 中间件函数按注册顺序执行。当中间件调用 c.Next() 时，它将控制权传递给下一个中间件（或最终处理函数），然后在 c.Next() 返回后继续执行。这创建了一个类似栈的（LIFO）模式——第一个注册的中间件最先开始但最后结束。如果中间件不调用 c.Next()，后续的中间件和处理函数将被跳过（这对于使用 c.Abort() 短路请求很有用）

	在 Gin 框架中，中间件的执行顺序遵循特定规则，这对于理解和正确使用中间件非常重要
		1. 全局中间件的执行顺序
			注册顺序：全局中间件按照注册到 Gin 引擎的顺序依次执行。
			例如，如果你先注册了日志记录中间件，然后注册了身份验证中间件，那么日志记录中间件会先执行，接着是身份验证中间件
		2. 分组中间件的执行顺序
			组内顺序：分组中间件先于组内的路由级中间件和处理函数执行。
			当一个请求匹配到某个路由组时，会先执行该路由组定义时传入的中间件。
		3. 路由级中间件的执行顺序
			特定路由执行：路由级中间件在所属路由匹配成功后，按照在路由定义中声明的顺序执行，并且在该路由的处理函数之前执行。
		4. c.Next() 与执行流程
			调用 c.Next() 后：当中间件调用 c.Next() 时，它会暂停当前中间件的执行，将控制权传递给下一个中间件（如果有）或路由处理函数。
			当路由处理函数或后续中间件执行完毕并返回后，c.Next() 之后的代码会继续执行
		5. 整体执行顺序总结
			1. 所有全局中间件按照注册顺序依次执行，直到某个中间件没有调用 c.Next() 或者到达最后一个全局中间件
			2. 如果请求匹配到一个路由组，该路由组的分组中间件按照定义顺序依次执行，直到某个分组中间件没有调用 c.Next() 或者到达最后一个分组中间件
			3. 对于特定路由，该路由的路由级中间件按照定义顺序依次执行，直到某个路由级中间件没有调用 c.Next() 或者到达最后一个路由级中间件
			4. 执行该路由的处理函数
			5. 处理函数返回后，按照与执行顺序相反的顺序，依次执行每个中间件中 c.Next() 之后的代码，即先执行路由级中间件中 c.Next() 之后的代码，然后是分组中间件中 c.Next() 之后的代码，最后是全局中间件中 c.Next() 之后的代码
*/
func UsingMiddleware(c *gin.Context) {
	c.JSON(http.StatusOK, gin.H{"message": "中间件的执行顺序"})
}
